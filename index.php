							<li id="chap1">Chapter 1: The Invisible Chains
								<div id="chap1Preview" class="text-muted text-justify mt-2 mb-2 merriweather">
									<cite>“Addictions ... started out like magical pets, pocket monsters. They did extraordinary tricks, showed you things you hadn't seen, were fun. But came, through some gradual dire alchemy, to make decisions for you. Eventually, they were making your most crucial life-decisions. And they were ... less intelligent than goldfish.”</cite>
									<p class="text-right font-weight-bold">William Gibson</p>

									<h6 class="text-center font-weight-bold">The Sedated Life<h6>

									<p>You wake up, your head feels groggy. You have little energy. After forcing yourself to get up, within an hour, you have a smoke.</p>

									<p>You slowly go about your day. Maybe you work, maybe you don’t. Conversation is difficult; you are unable to look people in the eye, unable to even string a coherent sentence together. You feel ashamed that you have little-to-no social skills. You never laugh or smile anymore; you seem to be in a permanent state of numbness. All you are concerned about is looking towards the next high, even though each subsequent high of the day is less and less effective at making you feel ok.</p>

									<p>Evening rolls around, and once again you find yourself in front the TV or computer, numbing and distracting yourself further. You gorge yourself on take-away food and unhealthy snacks. Any tasks or jobs you had planned have been left for another day.</p>

									<p>This terrible cycle continues day in, day out. You alternate between momentarily feeling good, before returning to that familiar state: lethargic, fuzzy, empty, numb, and above all, unhappy with life. Each day, you find that a little part of you dies; each day, you get slightly further away from your real self.</p>

									<p>Sometimes, you wonder if there is another life out there, away from the weed, away from the lonely, monotonous existence that your life has become. For a brief moment, you stop pretending that life is all good, and you begin to gaze upon the reality you have been blinding yourself to: you have neglected your life.</p>

									<p>You have traded in real growth and development in favour of a meaningless, artificial euphoria, kidding yourself that all is fine, when the reality is anything but. As the discomfort of this truth starts to set in, your brain persuades you why you should continue to smoke: it helps open your mind, it’s a social activity, it helps you be creative, it helps take the edge off the futility of life, it’s harmless, everyone does it, it’s not heroin, and so on, and so on.</p>

									<p>You reach for your stash box once again. The cycle continues.</p>

									<p>There must be more to life than this. There is.</p>
								</div>
